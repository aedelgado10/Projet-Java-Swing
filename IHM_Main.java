    private void se_connecter(){
        String id;
        char[] mdp;
        
        id = text_identifiant.getText();
        mdp = text_mdp.getPassword();
        
        String s_mdp = Main.charToString(mdp);
        
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        Integer userType = Utilisateur.verifier(id, s_mdp, this.e);  
        
        if (userType != null){
            this.setVisible(false);
            IHM.charger(userType);
        }
        else{
            connection_status.setText("identifiant ou mot de passe ErronÃ©");
        }        
    }
    
